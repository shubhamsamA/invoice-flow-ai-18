/**
 * PDF Export — renders a full invoice into a styled HTML page and triggers print-to-PDF.
 * Supports business branding (logo, stamp, signature) and layout_json template rendering.
 */

interface SimpleInvoiceData {
  client: string;
  items: { name: string; qty: number; price: number }[];
  gst: number;
  discount: number;
}

interface FullInvoiceData {
  invoice_number: string;
  issue_date: string;
  due_date?: string | null;
  status: string;
  subtotal: number;
  discount: number;
  gst_rate: number;
  gst_amount: number;
  total: number;
  currency: string;
  notes?: string | null;
  client_name?: string;
  client_email?: string;
  client_address?: string;
  client_gst?: string;
  items: { name: string; description?: string; quantity: number; unit_price: number; amount: number }[];
  // Business branding
  business_name?: string;
  business_email?: string;
  business_address?: string;
  business_gst?: string;
  logo_url?: string;
  stamp_url?: string;
  signature_url?: string;
  bank_account_name?: string;
  bank_account_number?: string;
  bank_ifsc?: string;
  bank_name?: string;
  bank_branch?: string;
  bank_upi_id?: string;
  layout_json?: any;
}

const fmt = (n: number, currency = "INR") => {
  if (currency === "USD") return `$${n.toLocaleString("en-US", { minimumFractionDigits: 2 })}`;
  return `₹${n.toLocaleString("en-IN", { minimumFractionDigits: 2 })}`;
};

